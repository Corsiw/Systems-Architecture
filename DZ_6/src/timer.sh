#!/bin/bash

# Обратный отсчет с использованием цикла while

echo "=== ПРОГРАММА ОБРАТНОГО ОТСЧЕТА ==="

# Проверка наличия аргумента
if [ $# -eq 0 ]; then
    echo "Использование: $0 <секунды>"
    echo "Пример: $0 10"
    exit 1
fi

count=$1

if ! [[ $count =~ ^[0-9]+$ ]]; then
    echo "Ошибка: Введите положительное целое число"
    exit 1
fi

echo "Начинаем обратный отсчет от $count секунд..."

while [ $count -gt 0 ]; do
    echo "Осталось: $count сек."
    sleep 1
    count=$((count - 1))
done

echo "ОТСЧЕТ ЗАВЕРШЕН!"